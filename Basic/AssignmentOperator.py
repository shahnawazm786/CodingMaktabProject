# Assignment Operator (+=, -=,*=,/= and %=)
a=100
a+=200 #=> a=a+200 => a=100+200 => a=300
print(a)
b=200
b-=100 # b= b- 100 => b= 200 - 100 => b= 100
print(b)
c=50
c*=10 # c = c* 10 => c= 50 *10 => c=500
print(c)
d=200
d/=25 # d= d/ 25 => d= 200/25 => 8
print(d)

e= 210
e%=25 # e = e%25 => e= 210 %25  = 10
print(e)

